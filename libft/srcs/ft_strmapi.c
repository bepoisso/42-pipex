#include "../includes/libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned	int, char))
{
	unsigned int	i;
	char			*dest;

	i = 0;
	if (!f)
		return (ft_strdup(""));
	dest = malloc(sizeof(char) + (ft_strlen(s)));
	if (!dest)
		return (NULL);
	while (s[i])
	{
		dest[i] = f(i, s[i]);
		i++;
	}
	dest[i] = '\0';
	return (dest);
}
